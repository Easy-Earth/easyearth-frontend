export function pickTitleBgPreset(grade, index) {
  const list = TITLE_BG_PRESETS[grade] ?? TITLE_BG_PRESETS.common;
  return list[index % list.length];
}

export function getTitleBgPresetById(presetId) {
  if (!presetId) return null;

  for (const grade of Object.keys(TITLE_BG_PRESETS)) {
    const found = TITLE_BG_PRESETS[grade].find((p) => p.id === presetId);
    if (found) return { grade, preset: found };
  }
  return null;
}